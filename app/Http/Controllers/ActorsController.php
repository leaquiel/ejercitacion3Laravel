    public function result(Request $request)
    {
      // dd($request);
      $queryString = $request->actors;

      $results = Actor::where("first_name", "LIKE", "$queryString%")
        ->orWhere("last_name", "LIKE", "$queryString%")
        ->get();

        return view('actors.result', compact('results'));


